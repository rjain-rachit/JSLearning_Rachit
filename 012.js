// Booleans and Comparison Operator

let num1 = 7;
let num2 = 8;
console.log(`${num1}<${num2} -> ${num1<num2}`)
console.log(`${num1}>=${num2} -> ${num1>=num2}`)

// == vs === 
num1 = 7;
num2 = "7";
console.log(`Only Value Check- ${num1}=="${num2}" -> ${num1==num2}`)
console.log(`Value & Datatype Check- ${num1}==="${num2}" -> ${num1===num2}`)

// != vs !==
console.log(`Only Value Check- ${num1}!="${num2}" -> ${num1!=num2}`)
console.log(`Value & Datatype Check- ${num1}!=="${num2}" -> ${num1!==num2}`)